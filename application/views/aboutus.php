                                <p style="text-align: justify;">Pertamina senantiasa bekerja keras membangun bangsa dengan mengokohkan komitmen dalam bidang energi baru dan terbarukan serta diversifikasi usaha.</p>
                                <p style="text-align: justify;">PT Pertamina (Persero) telah menempuh enam dekade dalam industri energi.&nbsp; Komitmen ini dibuktikan dengan penyediaan produk yang lebih berkualitas guna memenuhi kebutuhan konsumen akan produk yang unggul. Kini saatnya, Pertamina memantapkan langkah, menyongsong tantangan yang membentang dengan penuh optimisme guna menciptakan pertumbuhan bisnis Perusahaan yang berkelanjutan melalui investasi dan optimalisasi bisnis agar terus tumbuh sesuai dengan harapan seluruh pemangku kepentingan.</p>
                                <p style="text-align: justify;">Tonggak sejarah Pertamina diawali sekitar tahun 1950-an, Pemerintah Republik Indonesia menunjuk Angkatan Darat yang kemudian mendirikan PT Eksploitasi Tambang Minyak Sumatera Utara untuk mengelola lading minyak di wilayah Sumatera. Pada 10 Desember 1957, perusahaan tersebut berubah nama menjadi PT Perusahaan Minyak Nasional, disingkat PERMINA. Tanggal ini diperingati sebagai lahirnya Pertamina hingga saat ini. Pada 1960, PT Permina berubah status menjadi Perusahaan Negara (PN) Permina. Kemudian, PN Permina bergabung dengan PN Pertamin menjadi PN Pertambangan Minyak dan Gas Bumi Negara (Pertamina) pada 20 Agustus 1968.</p>
                                <p style="text-align: justify;">Selanjutnya, pemerintah mengatur peran Pertamina untuk menghasilkan dan mengolah migas dari ladangladang minyak serta menyediakan kebutuhan bahan bakar dan gas di Indonesia melalui UU No.8 tahun 1971. Kemudian melalui UU No.22 tahun 2001, pemerintah mengubah kedudukan Pertamina sehingga penyelenggaraan <em>Public Service Obligation </em>(PSO) dilakukan melalui kegiatan usaha.</p>
                                <p style="text-align: justify;">Berdasarkan PP No.31 Tahun 2003 tanggal 18 Juni 2003, Perusahaan Pertambangan Minyak dan Gas Bumi Negara berubah nama menjadi PT Pertamina (Persero) yang melakukan kegiatan usaha migas pada Sektor Hulu hingga Sektor Hilir. PT Pertamina (Persero) didirikan pada tanggal 17 September 2003 berdasarkan Akta Notaris No.20 Tahun 2003. Pada 10 Desember 2005, Pertamina mengubah lambing kuda laut menjadi anak panah dengan warna dasar hijau, biru, dan merah yang merefleksikan unsur dinamis dan kepedulian lingkungan.</p>
                                <p style="text-align: justify;">PT Pertamina (Persero) melakukan transformasi fundamental dan usaha Perusahaan pada 20 Juli 2006. PT Pertamina (Persero) mengubah visi Perusahaan yaitu, “menjadi perusahaan minyak nasional kelas dunia“</p>
                                <p style="text-align: justify;">Pertamina melalui anak usaha PT Pertamina International EP mengakuisisi saham perusahaan migas Prancis Maurel et Prom (M&amp;P) dengan kepemilikan saham sebesar 72,65% saham.</p>
                                <p style="text-align: justify;">pada tanggal 10 Desember 2007. Kemudian tahun 2011, Pertamina menyempurnakan visinya, yaitu “menjadi perusahaan energi nasional kelas dunia“. Melalui RUPSLB tanggal 19 Juli 2012, Pertamina menambah modal ditempatkan/disetor serta memperluas kegiatan usaha Perusahaan.</p>
                                <p style="text-align: justify;">Pada 14 Desember 2015, Menteri BUMN selaku RUPS menyetujui perubahan Anggaran Dasar Pertamina dalam hal optimalisasi pemanfaatan sumber daya, peningkatan modal ditempatkan dan diambil bagian oleh negara serta perbuatan-perbuatan Direksi yang memerlukan persetujuan tertulis Dewan Komisaris. Perubahan ini telah dinyatakan pada Akta No.10 tanggal 11 Januari 2016, Notaris Lenny Janis Ishak, SH.</p>
                                <p style="text-align: justify;">Pada 2017, salah satu langkah nyata mewujudkan visi menjadi perusahaan energi nasional kelas dunia adalah keberhasilan menuntaskan akuisisi saham perusahaan migas Prancis Maurel et Prom (M&amp;P). Terhitung mulai 1 Februari 2017 melalui anak usaha PT Pertamina International EP, Pertamina menjadi pemegang saham mayoritas M&amp;P dengan 72,65% saham. Melalui kepemilikan saham mayoritas di M&amp;P, Pertamina memiliki akses operasi di 12 negara yang tersebar di 4 benua. Pada masa mendatang, Pertamina menargetkan produksi 650 ribu BOEPD (<em>Barrels of Oil Equivalents Per Day</em>) di 2025 dari operasi internasional, sebagai bagian dari target produksi Pertamina 1,9 juta BOEPD di 2025, dalam upaya nyata menuju ketahanan dan kemandirian energi Indonesia.</p>
